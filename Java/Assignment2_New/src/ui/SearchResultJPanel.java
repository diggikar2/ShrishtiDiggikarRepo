    private void populateCarsByYearTable() {
        DefaultTableModel tcb = (DefaultTableModel) tblCarInfo.getModel();
        tcb.setRowCount(0);
                
        for(CarInfo cars: this.carList){
            Object[] row =  new Object[8];
            row[0]= cars;
//            row[1]=cars.getCompany();
            row[1]=cars.getModel();
            row[2]=cars.getNoOfSeats();
            row[3] = cars.getManufactureYear();
            row[4] = cars.getSerialNo();
            row[5] = cars.getAvailabilityStatus();
            row[6] = cars.getCity();
            row[7] = cars.getCertificateExpirationDate();
            tcb.addRow(row);
        }
        
        
    }
