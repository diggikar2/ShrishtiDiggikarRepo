    private void btnAttachBiometricIdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachBiometricIdenActionPerformed
        JFileChooser browse1 = new JFileChooser();
        FileNameExtensionFilter extensionFilter1 = new FileNameExtensionFilter("Only jpg and jpeg Supported", "jpeg", "jpg");
        browse1.setFileFilter(extensionFilter1);
        browse1.showOpenDialog(null);
        File f1 = browse1.getSelectedFile();
        String filename1 = f1.getName();
        txtBiometricIdenPath.setText(filename1);
        picture1 = new BufferedImage(100, 100, BufferedImage.TYPE_INT_ARGB);

        try {
            picture1 = ImageIO.read(f1);
        } catch (IOException ex) {
            Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        product.setBiometricIden(picture1);
    }//GEN-LAST:event_btnAttachBiometricIdenActionPerformed


