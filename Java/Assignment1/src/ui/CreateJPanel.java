    private void btnAttachPhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachPhotoActionPerformed
        JFileChooser browse = new JFileChooser();
        FileNameExtensionFilter extensionFilter = new FileNameExtensionFilter("Only jpg and jpeg Supported", "jpeg", "jpg");
        browse.setFileFilter(extensionFilter);
        browse.showOpenDialog(null);
        File f = browse.getSelectedFile();
        String filename = f.getName();
        txtUploadPic.setText(filename);
        picture = new BufferedImage(100, 100, BufferedImage.TYPE_INT_ARGB);

        try {
            picture = ImageIO.read(f);
        } catch (IOException ex) {
            Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        product.setPhoto(picture);
    }//GEN-LAST:event_btnAttachPhotoActionPerformed

