    private void addVerifiers() {
        InputVerifier integerVerifier = new MyNumericVerifier();
        txtAge.setInputVerifier(integerVerifier);
        InputVerifier stringVerifier = new MyStringVerifier();
        txtFirstName.setInputVerifier(stringVerifier);
        InputVerifier stringVerifier1 = new MyStringVerifier();
        txtLastName.setInputVerifier(stringVerifier1);
        InputVerifier stringVerifier2 = new MyStringVerifier();
        txtCommunity.setInputVerifier(stringVerifier2);
    }

    private void populatePatientDetails() {
         txtFirstName.setText(person.getFirstName());
         txtLastName.setText(person.getLastName());
         txtAge.setText(String.valueOf(person.getAge()));
         txtResidence.setText(person.getResidence());
         txtCommunity.setText(person.getCommunity());
    }

    private void modifyTextFields(Boolean isEdit) {
        if (isEdit) {
            txtFirstName.setEnabled(true);
            txtLastName.setEnabled(true);
            txtAge.setEnabled(true);
            txtResidence.setEnabled(true);
            txtCommunity.setEnabled(true);
        } else {
            txtFirstName.setEnabled(false);
            txtLastName.setEnabled(false);
            txtAge.setEnabled(false);
            txtResidence.setEnabled(false);
            txtCommunity.setEnabled(false);
        }
    }

    private boolean checkBlankInput() {
        if (txtAge.getText().length() == 0
                || txtFirstName.getText().length() == 0
                || txtLastName.getText().length() == 0
                || txtResidence.getText().length() == 0
                || txtCommunity.getText().length() == 0) {
            return false;
        } else {
            return true;
        }
    }
