    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) customerListJTable.getModel();
        dtm.setRowCount(0);
        for (String s : this.ecosystem.getCustomerDirectory().getCustomerList().keySet()) {
            Customer c = this.ecosystem.getCustomerDirectory().getCustomerList().get(s);
            Object row[] = new Object[3];
            row[0] = c.getCustId();
            row[1] = c.getFirstName();
            row[2] = c.getCustomerAccount().getOrders().getOrderDirectory().size();
            dtm.addRow(row);
        }
    }
